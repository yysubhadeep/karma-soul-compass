// Updated function to filter cities with better search capabilities
export const filterCities = (searchTerm: string): string[] => {
  if (!searchTerm || searchTerm.length < 2) return [];
  
  const lowercaseSearch = searchTerm.toLowerCase();
  
  // First priority: Cities that start with the search term
  const startsWithMatches = indianCities.filter(city => 
    city.toLowerCase().startsWith(lowercaseSearch)
  );
  
  // Second priority: Cities that contain the search term
  const containsMatches = indianCities.filter(city => 
    city.toLowerCase().includes(lowercaseSearch) && 
    !city.toLowerCase().startsWith(lowercaseSearch)
  );
  
  // Combine and limit results
  const combinedResults = [...startsWithMatches, ...containsMatches];
  return combinedResults.slice(0, 10); // Increased to 10 suggestions
};

